export const LANGUAGE_COOKIE = 'mausamog-language';
export const LANGUAGE_STORAGE_KEY = 'mausamog.language';
export const DEFAULT_LANGUAGE = 'en';

export const supportedLanguages = ['en', 'hi', 'kn', 'ta'] as const;

export type SupportedLanguage = (typeof supportedLanguages)[number];

export const languageLabels: Record<SupportedLanguage, string> = {
  en: 'English',
  hi: 'हिन्दी',
  kn: 'ಕನ್ನಡ',
  ta: 'தமிழ்',
};

export const formLanguageValues: Record<SupportedLanguage, string> = {
  en: 'English',
  hi: 'Hindi',
  kn: 'Kannada',
  ta: 'Tamil',
};

export function isSupportedLanguage(value: string | undefined): value is SupportedLanguage {
  return supportedLanguages.includes(value as SupportedLanguage);
}

export function normalizeLanguage(value: FormDataEntryValue | string | null | undefined): SupportedLanguage {
  const raw = String(value ?? '').trim().toLowerCase();

  if (isSupportedLanguage(raw)) return raw;
  if (raw === 'english') return 'en';
  if (raw === 'hindi') return 'hi';
  if (raw === 'kannada') return 'kn';
  if (raw === 'tamil') return 'ta';

  return DEFAULT_LANGUAGE;
}

